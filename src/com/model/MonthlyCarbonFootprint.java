package com.model;

public class MonthlyCarbonFootprint {
	private int month;
	private double month_totalCF;
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public double getMonth_totalCF() {
		return month_totalCF;
	}
	public void setMonth_totalCF(double month_totalCF) {
		this.month_totalCF = month_totalCF;
	}
}
